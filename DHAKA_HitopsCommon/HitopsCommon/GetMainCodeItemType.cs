#region

using System;

#endregion

namespace HitopsCommon
{
    static public  class GetMainCodeItemType
    {
        static String mainCod       ="MAIN_COD";
        static String codName       ="COD_NAME";
        static String codDesc       ="COD_DESC";
        static String codNameDesc   ="COD_NAME_DESC";
        static String mainCodDesc   ="MAIN_COD_DESC";

        public static String MAIN_COD { get { return mainCod; } }
        public static String COD_NAME { get { return codName; } }
        public static String COD_DESC { get { return codDesc; } }
        public static String COD_NAME_DESC { get { return codNameDesc; } }
        public static String MAIN_COD_DESC { get { return mainCodDesc; } }

    }
}
